  <p>Vita-Cal Food Supplement is a high calorie supplement that works to provide comprehensive nutrition to supplement nutrition, caloric intake, or both. This gel is veterinarian approved for supplemental feeding and entices pets with a palatable malt flavor that makes it easy to feed directly or to mix in with your pet's food. This supplement contains a comprehensive list of vitamins and minerals and contains 28 kcals per 6 gram serving.</p>
  
  <p>Vita-Cal Food Supplement is perfect for pets that are having difficulty eating full meals after a medical procedure or that aren't eating properly due to anxiety or stress. This supplement is best used for pets that need both calories and nutrients, though it can act as a supplement for pets that need only nutrients or only calories, too. Your pet will love this dense flavored gel- so order now!</p>
 
  
