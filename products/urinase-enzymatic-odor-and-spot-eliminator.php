<p><h2>Administration:</h2>
Shake well before using. Test carpet fabrics for colorfastness prior to use. Remove excess urine by wiping or other method, saturate contaminated area with Urinase Enzymatic. Blot with a dry towel until no transfer occurs. Repeat if needed. Follow with water rinsing if needed, allow to air dry.</p>
  
<p><h2>Applications:</h2>Urinase Enzymatic eliminates types of spots and odors such as urine, vomit, feces, grease, blood, etc. Urinase Enzymatic works best on carpeting, hard surface flooring, furniture and upholstery, bedding crates, carriers and cages, kennels, dog runs, horse trailers, stables, marking areas, litter boxes, cars and trucks adn other areas where stubborn spots and odors reside. Also aids in housebreaking pets by eliminating the residual odors that 'trigger' marking instincts.</p>
  
  <p><h2>Warnings:</h2> For animal use only. Keep out of the reach of children and animals. In case of accidental overdose, contact a health professional immediately.</p> 
