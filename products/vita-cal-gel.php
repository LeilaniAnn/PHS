 </div><h2>For Use In:</h2> Dogs and Cats</p>
  <p><h2>Administration:</h2>
   Feed measured amount directly to pet. The product can be fed directly from the finger, from a dish, or mixed with food.</p> 

<p><h2>Maintenance Administration:</h2>Feed a 3¾ inch ribbon = 1½ teaspoons per 10 lbs of bodyweight daily.</p>

  
  
  <p>For Supplemental Feeding Only.</p> 
  
  <p>Keep container at room temperature (59&deg;F-86&deg;F). Manufactured in the USA.</p>
  
  <p>For animal use only. Keep out of the reach of children and animals. In case of accidental overdose, contact a health professional immediately.</p> 
  
