 </div><h2>For Use In:</h2> Dogs and Cats</p>
  <p><h2>Administration:</h2>
   Apply liberally into the ear canal. Massage the base of the ear. Allow pet to shake head. Clean excess with a cotton ball.</p> 
  
<p><h2>Loading Administration:</h2> 
In excessively dirty ears: apply 2-3 times daily over several days.</p>

<p><h2>Maintenance Administration:</h2> Apply 1-2 times weekly or as often as recommended by your veterinarian. Always apply after swimming.</p>
  
  
  <p>
  <h2>Cautions:</h2> Keep container at room temperature (59&deg;F-86&deg;F).</p>
  
  <p><h2>Warnings:</h2>
  For animal use only. For topical use only.  Avoid contact with eyes.  In case of contact, flush eyes with water and seek medical attention if irritation persists.  Do not use in ears with ruptured tympanic membranes (eardrums). Keep out of the reach of children and animals. In case of accidental overdose, contact a health professional immediately.
