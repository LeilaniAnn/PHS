<p>After 3 weeks loading dose, if positive results are seen, reduce to maintenance dose. Since each pet's body is different, results may take longer in some pets. If positive results are not seen, continue loading dose.</p>
  
  
<p><h2>Cautions:</h2> 
Safe use in pregnant animals or animals intended for  breeding has not been proven.  If lameness worsens, discontinue use and contact your veterinarian. Administer during or after the animal has eaten to reduce incidence of gastrointestinal upset.  An examination from a veterinarian is recommended prior to using this product. Not for use with anticoagulants. Not recommended for long term use.
  </p>
<p>Avoid Excess Heat. Keep container at room temperature (59&deg;F-86&deg;F). Manufactured in the USA.</p>
  
  <p><h2>Warnings:</h2> For animal use only. Keep out of the reach of children and animals. In case of accidental overdose, contact a health professional immediately.</p> 
  
