  <h1>Rapid-Care G.I.<sup>&trade;</sup> Paste &#124; For Dogs & Cats </h1>
  
  <p>Proflora veterinary-strength probiotic soft chews for dogs help support digestion and overall canine health. For daily use. Proflora™ helps maintain a healthy probiotic balance in the digestive system. Use Proflora™ to re-balance the flora population in your dog's gut. Contains 9 strains of microorganisms and 1 billion CFUs per dose.</p>

  <p>Proflora Helps With:</p>
  
  <ul>
    <li>Gas</li>
    <li>Runny Stool</li>
    <li>Constipation</li>
    <li>Scooting</li>
    <li>Difficulty Digesting</li>
    <li>Difficulty Absorbing Nutrients</li>
    <li>Stomach Cramps</li>
    <li>Low Immune System</li>
    <li>Skin and Coat</li>
    <li>Bad Breath</li>
  </ul>
  
  <p>There are many ways the balance of good verses bad bacteria in the gut can become imbalanced. Diet, stress, medications and especially antibiotics are just a few of the ways. Having an unbalanced gut flora or no flora in the gut (after antibiotics) can lead to multiple problems; Gas, Runny Stool, Constipation, Scooting, Difficulty digesting, in ability to absorb nutrients, stomach cramps and can lead to other problems like low immune system and unhealthy skin and coat, even bad breath. Using the 9 strains of beneficial bacteria in Proflora™ on a daily basis helps repopulate the gut with bacteria that replace harmful bacteria, and aid in digestion. Proflora comes with FOS, a non-digestable prebiotic that acts as food for the probiotics.</p>
  
