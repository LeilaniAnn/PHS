  <p><h2>Cautions:</h2>
  Safe use in pregnant animals or animals intended for breeding has not been proven.  If lameness worsens, discontinue use and contact your veterinarian. Administer during or after the animal has eaten to reduce incidence of gastrointestinal upset.
  </p>
  An examination from a veterinarian is recommended prior to using this product. Not recommended for long term use.
  </p>
  Not for use with anticoagulants.
  Use with caution in conjunction with CNS depressants; do not use prior to anesthesia. May be GI irritant and not used in high doses with anticoagulant drugs or patients with stomach ulcers.
