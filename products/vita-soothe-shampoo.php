  <p><h2>Administration:</h2>
 Shaking before use is not necessary. The active ingredients in Vita-Soothe® stay in suspension to ensure effective results with each bathing. Wet pet thoroughly with warm water. Apply enough Vita-Soothe® Shampoo to make a rich lather. Work shampoo from head to hindquarters. Let stand for approximately 5-10 minutes then rinse thoroughly. For best results repeat process.</p>
  
