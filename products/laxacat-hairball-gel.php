  <h1>LaxaCat<sup>&trade;</sup> &#124; Hairball Petroleum Free Gel</h1>
  
  <p>LaxaCat Hairball Gel is a petroleum-free gel that uses natural ingredients to eliminate hairballs in cats. This gel is infused with natural chamomile to soothe discomfort caused by the digestive problems associated with hairballs. Other ingredients also promote a healthy skin and coat to prevent the hairballs at their cause by limiting hair loss and the hair digested during your cat’s natural grooming.</p>
  
  <p>LaxaCat Hairball Gel is made with nutrients that inhibit hairball formation and eliminate existing hairballs by softening the hairball and strengthening the digestive tract to pass the hairball safely. This product comes with three ounces of gel, but the same supplement is also available as a soft chew. You’ll love the way this product combats hairballs and keeps your cat healthy.</p>
