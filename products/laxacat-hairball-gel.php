  <p>LaxaCat Hairball Gel Petroleum Free is a palatable chicken-favored gel for the maintenance of a healthy digestive tract. It is recommend to help facilitate the elimination of hairballs and help minimize the formation of new ones. Hairball Gel PF is petrolatum free and contains natural ingredients. Beeswax and soybean oil help emulsify and soften the hairballs, as well as sheathes the digestive tract. Chamomile helps soothe sensitive stomach and digestive tract. Hairball Gel PF can also be used to help facilitate regular bowel movements in animals prone to constipation.</p>
