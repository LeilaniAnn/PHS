  <h1>Oral-Clens<sup>&reg;</sup> &#124; Toothpaste  </h1>
  
  <p>PHS Ora-Clens Dental Gel Toothpaste works to freshen breath and gently clean your pet's teeth and gums. This enzymatic toothpaste uses ingredients to catalyze the effects of the baking soda solution to kill bacteria that can lead to dental disease. The dental gel also features a delightful mint flavor to provide a pleasing taste to your pet while brushing and to freshen breath. The gel is also safe for cats and dogs of all sizes.</p>
  <p>Ora-Cleans Dental Gel Toothpaste is ideal for multiple pet households that need regular home dental care for each pet. Each package of the dental gel comes with 4 ounces of toothpaste that should be applied with a toothbrush. You'll love the way this gel cleans your pet's teeth and gums.</p>
