<p><h2>Administration:</h2>
Feed soft chews directly to cat or crumble over food.</p>
  
  <p><h2>Maintenance Administration:</h2> 
  To eliminate hairballs, feed up to four pieces every day until hairball is eliminated. To prevent hairballs, feed up to four pieces twice weekly along with regular brushing of the cat’s coat.</p>
  
