  <h1>LaxaCat<sup>&trade;</sup> &#124; Hairball Remedy Soft Chews</h1>
  
  <p>LaxaCat™ hairball remedy for cats is a tasty salmon formula to help eliminate hairballs and their symptoms, including constipation, straining during bowel movement, dry cough and vomiting after meals. Although this product comes in a soft chew form, with 60 soft chews included with each bottle, there is also a gel form of the supplement that is petroleum-free.</p>
  
  <p>LaxaCat™ also helps limit the incidence of hairballs (A.K.A. furballs) by supporting a healthy skin and coat. Contains no added salt or artificial colors.</p>
