<p><h2>Cautions:</h2>
 Safe use in pregnant animals or animals intended for breeding has not been established. If animal's condition worsens or does not improve, stop product administration and consult your veterinarian. Examination by a veterinarian is recommended prior to use. </p>
  
 <p>Avoid Excess Heat. Keep container at room temperature (59&deg;F-86&deg;F).</p>
  
  <p><h2>Warnings:</h2> For animal use only.  Keep out of reach of children.  In case of accidental overdose, contact a health professional immediately. If animal's condition worsens or does not imporve, stop product administration and consult your veterinarian.</p> 
  
