  <h1>Resvive<sup>&trade;</sup> &#124; for Large Dogs</h1>
  
  <p>Resvive contains the active ingredients resveratrol and hyaluronic acid. Resveratrol is a potent antioxidant that has been proven to reduce gene expression of inflammatory mediators. Hyaluronic acid is a primary component of joint-lubricating synovial fluid and can be diminished in connective tissues with aging.</p>
  
