  <p>Syner-G® Digestive Enzymes aids in digestion and nutrient absorption. Also helps with gas reduction, bloating, prevent coprophagy (the unhealthy habit of stool eating), allergies and shedding in dogs and cats.</p>
  
  <p>Functionality: Syner-G® Digestive Enzymes is a plant derived food enzyme supplement utilizing a proprietary formulation of specially cultured enzymes to hydrolyse starches, proteins and triglycerides to break down cellulose.  Syner-G® Digestive Enzymesis for dogs and cats only.</p>
  
