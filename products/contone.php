<p><h2>Cautions:</h2>
   Safe use in pregnant animals or animals intended for breeding has not been proven. If animal's condition worsens or does not improve, stop product administration and consult your veterinarian. An examination from a veterinarian is recommended prior to using this product. Not to be used with animals experiencing diarrhea. Not for use in animals with hypoglycemia. Use with caution with hypotension; not to be used with hypotensive drugs or prior to anesthesia.
  
   <p>Avoid Excess Heat. Keep container at room temperature (59°F-86°F). Manufactured in the USA, made within an FDA and USDA approved facility following current National Sanitation Foundation certified Good Manufacturing Practices (NSF cGMP) under strict quality control specifications.</p>
  
  <p><h2>Warnings:</h2> For animal use only. Keep out of the reach of children and animals. In case of accidental overdose, contact a health professional immediately.</p> 
  
