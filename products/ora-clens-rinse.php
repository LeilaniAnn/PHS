  <h1>Oral-Clens<sup>&reg;</sup> &#124; Rinse  </h1>
  
  <p>An effective, safe and palatable, fresh antimicrobial formula for reducing plaque and freshening breath in dogs and cats. Contains antimicrobial, anti-plaque and anti-calculus properties to aid in tooth and gum disease prevention.</p>
  <p>1% Chlorhexidine Gluconate - This active ingredient is an effective antiseptic, helps to reduce dental plaque and oral bacteria which causes bad breath.</p>
  <p>Cetylpyridinium Chloride - This active ingredient is an antiseptic that kills bacteria and other microorganisms. It has been shown to be effective in preventing dental plaque and reducing gingivitis.</p>
  
