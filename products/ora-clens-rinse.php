 </div><h2>For Use In:</h2> 
 Dogs and Cats<p>
 <h2>Administration:</h2>
Shake well before each use. Hold bottle upright, below your petâ€™s field of vision. Gently lift the upper lip to expose the teeth and gums. Point and squeeze to apply a gentle stream along the gum line. Ora-Clens dispenses rapidly and completely covers the entire oral cavity, even hard-to-reach areas. Avoid touching the gum with the applicator tip to avoid injury in case your pet moves.
</p>
  
<p><h2>Maintenance Administration:</h2> Rinse daily following each meal or as directed by your Veterinarian.</p>
  
  
  <p><h2>Cautions:</h2> Avoid contact with eyes. If eye contact occurs, rinse thoroughly. If undue skin irritation develops or increases, discontinue use and consult a veterinarian.</p>
  <p>Keep container at room temperature (59&deg;F-86&deg;F). Manufactured in the USA.</p>
  
  <p><h2>Warnings:</h2> For animal use only. Keep out of the reach of children and animals. In case of accidental overdose, contact a health professional immediately.</p> 
