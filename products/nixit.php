 Dogs</p>
<p><h2>Administration:</h2>
Sprink over pet's food and mix in.</p>

<p><h2>Loading Administration:</h2> Give 1 scoop over food twice daily.</p>
  
<p><h2>Maintenance Administration:</h2> Give 1 scoop over food daily.</p>
  
  <p>Store in a dry place. Keep container at room temperature (59&deg;F-86&deg;F).</p>
  
  <p>For animal use only. Keep out of the reach of children and animals. In case of accidental overdose, contact a health professional immediately.</p> 
  
