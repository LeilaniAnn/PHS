  <h1>Nixits<sup>&trade;</sup> &#124; Stool Eating Preventative</h1>
  
  <p>Nixit Stool-Eating Preventative (10.5 oz) granules helps eliminate and prevent coprophagy, the unhealthy habit of stool eating. Also provides vitamin B complex to coprophagic pets. Vitamin B complex deficiency is a known problem in coprophagic pets.</p>
  
