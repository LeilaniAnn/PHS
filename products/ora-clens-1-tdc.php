  <h1>Ora-Clens<sup>&reg;</sup> | 1-TDC&reg; Periodontal</h1>
  
  <p>Ora-Clens 1-TDC Periodontal Soft Gels are the perfect way to maintain dental health in cats and dogs while ridding them of bad breath in the process. Periodontal disease afflicts more than 80% of animals in spite of being completely preventable with regular dental care. Brushing your pet's teeth can be a burden, but neglecting dental care altogether can shorten your pet's life by up to two years.</p>
  <p>Ora-Clens 1-TDC Periodontal contains a modified molecule, 1-tetradecanol complex, that is a clinically proven and veterinarian-recommended way to combat periodontal disease that boasts improved dental health in as little as two weeks. These soft gels are easy to apply and taste great to ensure that your pet will thoroughly enjoy caring for their teeth in the future. Don't let your pet suffer the ill effects of periodontal disease- order these easy-to-use, incredibly effective soft gels today!</p>
