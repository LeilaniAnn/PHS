  <h1>Joint MAX<sup><sup>&reg;</sup></sup> &#124; Double Strength Soft Chews</h1>
  
  <p>Joint MAX RS helps ease stiffness and promotes healthy cartilage and joints. Glucosamine and Chondroitin work together to promote improved joint health by strengthening your dog's cartilage. It also contains Vitamin C, Zinc and N-Acetyl-Cysteine for added joint healing. Recommended for dogs that show moderate signs of joint disease.</p>
  
  <p>You should use this if your pet is experiencing one of these symptoms:</p>
  
  <ul>
    <li>Stiffness after exercise</li>
    <li>Difficulty jumping or climbing stairs</li>
    <li>Walking more slowly</li>
    <li>Hopping instead of running</li>
  </ul>
  
  <p>Joint MAX can be used alone or in combination with prescription drugs like Rimadyl<sup>&reg;</sup>, Etogesic<sup>&reg;</sup>, or Deramaxx<sup>&reg;</sup> and can eventually help decrease the need for these drugs.</p>
  
