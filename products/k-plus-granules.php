  <h1>Cranberry K-Plus<sup>&trade;</sup> &#124; potassium citrate Plus Cranberry</h1>
  
  <p>K-Plus Potassium Citrate Plus Cranberry (300 g Granules) are a dietary supplement for use in dogs and cats that contain ingredients known to aid in the prevention of urinary stone formation. Potassium citrate helps decrease the possibility of calcium oxalate stone formation and cranberry extract has been shown to enhance urinary tract health.</p>
  
