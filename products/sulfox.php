 </div><h2>For Use In:</h2> 
 Dogs, Cats, and Horses</p>
  <p><h2>Administration:</h2>
 Shake well before use. Wet the hair coat with warm water. Apply and lather first on the affected area then apply and lather on the entire hair coat. Allow to remain on hair for 5 to 10 minutes then rinse thoroughly with clean water. Repeat if necessary.</p>
  
 <p> <h2>Maintenance Administration:</h2> May be used 2 to 3 times per week or as directed by your veterinarian. </p>
  
  
<p><h2>Cautions:</h2>For topical use in dogs, cats, and horses. Avoid contact with the eyes or mucous membranes. If contact occurs, rinse thoroughly with water and consult a veterinarian. If irritation develops, discontinue use and consult a veterinarian. </p>
  <p>Store in a cool, dry place. May bleach colored fabrics. Manufactured in the USA.</p>
  
  <p><h2>Warnings:</h2> For animal use only. Keep out of the reach of children and animals. In case of accidental overdose, contact a health professional immediately.</p> 

  
