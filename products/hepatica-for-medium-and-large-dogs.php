<p><h2>Administration:</h2>
Administer orally to the pet as a treat, or crumble over pet's food at mealtime at the measured dose. If crumbled over food, consumption should be monitored to ensure the full dosage is consumed. Hepatica chewable tablets are formulated with a highly palatable beef flavor to ensure all dogs and cats will readily consume the tablet.</p>
  
