  <h1>Hepatica<sup>&trade;</sup> &#124; For Medium and Large Dogs</h1>
  
  <p>Hepatica is a liver support formula for dogs and cats with no SAMe. A potent antioxidant formulation that provides liver function support and protection from various causes of liver disease, including oxidation. The main ingredient is silymarin, which is a complex of silybin, the main active constituent of milk thistle, and phosphatidylcholine - because there is no SAMe, there are less side effects for some dogs.</p>

  
