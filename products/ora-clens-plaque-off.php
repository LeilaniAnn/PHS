  <h1>Ora-Clens<sup>&reg;</sup> | Plaque Off</h1>
  
  <p>Ora-Clens Plaque Off works to fight the bacteria that produce plaque, tartar and bad breath in cats and dogs. Plaque and tartar can cause gum disease, tooth decay and other dental problems. This product uses natural ingredients to prevent these diseases at their source with a formula that contains no sugar, preservatives or gluten.</p>
 <p>Ora-Clens Plaque Off is simple to administer to your pet- simply mix a small dosage of powder into your dog's wet or dry food. This bottle comes with 40 grams of powder, though a larger container that holds 180 grams is also available. You'll love how well this product reduces your pet's bad breath and keeps her teeth and gums healthy, so order a bottle today!</p>
  
  
