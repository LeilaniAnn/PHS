  <h1>Proflora<sup>&reg;</sup> &#124; Daily Probiotic for Dogs</h1>
  
  <p>Proflora Probiotic for Dogs is a probiotic supplement that promotes a healthy bacterial balance in your pet's digestive tract. When administered daily, 200 million CFU of beneficial bacteria and the prebiotic, inulin, ensure that your pet's gastrointestinal tract has the ideal balance of microorganisms. This special formulation contains 4 strains of bacteria that promote healthy digestion and appetite.</p>
  
  <p>Proflora Probiotic for Dogs can help reduce instances of diarrhea, constipation, and gas while simultaneously promoting a healthy immune system.</p>
  <p>Key Features:</p>
  
  <ul>
    <li>Contains 200 million CFU of beneficial bacteria</li>
    <li>4 unique strains of probiotics for optimal balance of microflora</li>
    <li>Includes prebiotic inulin to work synergestically with probiotic blend</li>
    <li>Comes with 30 packages of 1-gram doses to mix into your pets food</li>
  </ul>
  
