 <p><h2>For Use In:</h2> Dogs, Cats, & Horses</p>
  <p><h2>Administration:</h2>
   Apply liberally to affected area. Use cotton or absorbent material to clean excess solution. Do not allow animal to lick the treated areas until dry to prevent ingestion.</p>
  

<p><h2>Maintenance Administration:</h2>Apply twice daily or as directed by your veterinarian. Wash hands after using.</p>
  
  
<p><h2>Cautions:</h2> 
Avoid contact with eyes or mucous membranes. If eye contact occurs, rinse thoroughly with water and consult a veterinarian. If undue skin irritation develops or increases, discontinue use and consult a veterinarian.</p> Wash hands after use.
  
  <p>Avoid Excess Heat. Keep container at room temperature (59&deg;F-86&deg;F). Manufactured in the USA.</p>
  
  <p><h2>Warnings:</h2> If undue skin irritation develops or increases, discontinue use and consult a veterinarian. For veterinary use only. Keep out of the reach of children.</p> 
  
