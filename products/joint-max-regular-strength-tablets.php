  <p>After 3 weeks loading dose, if positive results are seen, reduce to maintenance dose. Since each pet's body is different, results may take longer in some pets. If positive results are not seen, continue loading dose. Joint MAX Regular Strength Chewable Tablets can also be used long term on a daily basis for normal dogs and/or cats to help support joint health.</p>
  
