 <p><h2>For Use In:</h2> Dogs, Cats, & Horses</p>
  <p><h2>Administration:</h2>
   Thoroughly wipe affected area with a pledget wipe. Do not allow the animal to lick the treated areas until dry to prevent ingestion. Wipes should be used for one application only; do not reuse. Avoid use on open wounds, cuts, or raw areas (use flush instead).
   </p>
   <p><h2>Maintenance Administration:</h2> 
 Use 1 to 3 times per day, or as directed by your veterinarian.</p>
  
  
<p><h2>Cautions:</h2>
 Avoid contact with eyes or mucous membranes. If eye contact occurs, rinse thoroughly with water and consult a veterinarian. Wash hands after use. 
  
 <p>Avoid Excess Heat. Keep container at room temperature (59&deg;F-86&deg;F). Protect from freezing. Manufactured in the USA.</p>
  
  <p><h2>Warnings:</h2> If undue skin irritation develops or increases, discontinue use and consult a veterinarian. Available through licensed veterinarians only. Keep out of the reach of children.</p> 
  
