</div><h2>For Use In:</h2> 
 Dogs and Cats</p>
<p><h2>Administration:</h2> Administer orally to the pet as a treat, or crumble over pet's food at mealtime at the prescribed dose.</p>
  
 <p> <h2>Maintenance Administration:</h2>Give 1 tablet for each cup of food given to pet. Dosages should be doubled for companion animals eight years of age or older. </p>
  
  <p>Avoid Excess Heat. Keep container at room temperature (59&deg;F-86&deg;F). 
    
  <p>For animal use only. Keep out of the reach of children and animals. In case of accidental overdose, contact a health professional immediately.</p> 
  
