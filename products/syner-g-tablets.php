  <h1>Syner-G<sup>&reg;</sup> &#124; Food Enzyme Supplement Chewable Tablets</h1>
  
  <p>Syner-G® Digestive Enzymes aids in digestion and nutrient absorption. Also helps with gas reduction, bloating, prevent coprophagy (the unhealthy habit of stool eating), allergies and shedding in dogs and cats.</p>
  
  <p>Functionality: Syner-G® Digestive Enzymes is a plant derived food enzyme supplement utilizing a proprietary formulation of specially cultured enzymes to hydrolyse starches, proteins and triglycerides to break down cellulose. Its effectiveness has been scientifically proven. All ingredients are human food grade and generally recognized as safe (GRAS) by the US FDA. Syner-G® Digestive Enzymesis for dogs and cats only.</p>
  
