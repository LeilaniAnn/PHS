 </div><h2>For Use In:</h2> 
 Dogs, Cats, and Horses</p>
  <p><h2>Administration:</h2>
   Shake well before use. For dry application, apply directly and allow to dry on coat. For wet application or after shampooing, remove excess moisture and apply directly to skin by parting hairs, then continue grooming as usual.</p> 
  
<p><h2>Loading Administration:</h2> Use 2-3 times per week for the first 2 weeks.</p>

<p><h2>Maintenance Administration:</h2> As directed by veterinarian.</p>
  
  
  <p><h2>Cautions:</h2> For topical use only. Avoid contact with eyes. In case of eye contact, flush eyes with water and seek medical attention if irritation persists. </p> 
  
  <p>Manufactured in the USA.</p>
  
  <p><h2>Warnings:</h2> For animal use only. Keep out of the reach of children and animals. In case of accidental overdose, contact a health professional immediately.</p> 
  
