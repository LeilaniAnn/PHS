 <p><h2>For Use In:</h2> Dogs, Cats, & Horses</p>
  <p><h2>Administration:</h2>
   Shake well before use. Wet the hair coat with warm water and apply a sufficient amount of shampoo to create a rich lather. Massage shampoo into wet hair coat, lather freely. Rinse and repeat. Allow to remain on hair for 5 to 10 minutes, then rinse thoroughly with clean water.
   </p>

<p><h2>Loading Administration:</h2>May be used initially 2 to 3 times a week for 4 weeks.</p>  

<p><h2>Maintenance Administration:</h2> 
 Use once a week, or as directed by your veterinarian.</p>
  
  
<p><h2>Cautions:</h2> Avoid contact with eyes or mucous membranes. If eye contact occurs, rinse thoroughly with water and consult a veterinarian. Not for otic use. Wash hands after using.</p> 
  
<p>Avoid Excess Heat. Keep container at room temperature (59&deg;F-86&deg;F). Manufactured in the USA.</p>
  
  <p><h2>Warnings:</h2> If undue skin irritation develops or increases, discontinue use and consult a veterinarian. For veterinary use only. Keep out of the reach of children.</p> 
  
