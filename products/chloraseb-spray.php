 <p><h2>For Use In:</h2> Dogs, Cats, & Horses</p>
  <p><h2>Administration:</h2>
   Spray directly onto the affected area(s). Do not allow animal to lick the treated areas until dry to prevent ingestion.
   </p>
  

<p><h2>Maintenance Administration:</h2> 
Spray  up to 3 times daily, or as directed by veterinarian Wash hands after using.</p>
  
  
<p><h2>Cautions:</h2>
 For topical use on dogs, cats, and horses only. Avoid contact with eyes or mucous membranes. If eye contact occurs, rinse thoroughly with water and consult a veterinarian. Wash hands after use.
  
 <p>Avoid Excess Heat. Keep container at room temperature (59&deg;F-86&deg;F). Manufactured in the USA.</p>
  
  <p><h2>Warnings:</h2> If undue skin irritation develops or increases, discontinue use and consult a veterinarian. For veterinary use only. Keep out of the reach of children.</p> 
  
