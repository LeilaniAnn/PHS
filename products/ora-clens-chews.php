 Dogs</p>
<p><h2>Administration:</h2>
Administer orally. Give to dog to chew. It is recommended to monitor your dog while this product is being chewed. Gulping whole chews can be harmful to a dog.</p>
  
<p><h2>Maintenance Administration:</h2>Give 1 chew daily.</p>
   
  
<p>This product is recommended for intermittent or supplemental feeding only. Do not feed to cattle or ruminants.</p>
  
  <p>For animal use only. Keep out of the reach of children and animals. In case of accidental overdose, contact a health professional immediately.</p> 
