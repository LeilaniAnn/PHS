  <h1>Ora-Clens<sup>&reg;</sup> &#124; Oral Hygiene Chews </h1> 
  
  <p>Ora-clens Oral Hygiene Chews formula features a blend of the enzymes Lactoperoxidase and Glucose Oxidase, which help prevent plaque and tartar build up as well as promote clean teeth and fresh breath. With the enzymes coated onto the chews, your dog will gain faster benefit. Oral Hygiene Chews also feature an abrasive texture, which help scrub your dog's teeth while he chews.</p>
  <p>Maintaining your dog's oral health is an important part to maintaining your dog's overall health. Feeding your dog an Oral Hygiene Chew as a treat is the easiest part of the oral care process. Dogs love the delicious poultry flavor! Try Oral Hygiene Chews with other Ora-Clens products to optimize oral health.</p>
