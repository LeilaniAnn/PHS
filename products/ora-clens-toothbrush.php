  <h1>Oral-Clens<sup>&reg;</sup> &#124; Toothbrush</h1>
  
  <p>PHS Ora-Clens Dual Ended Toothbrush makes home dental care easy for owners and works for pets of all breeds and sizes. Each end features a brush that is a different size to ensure that dogs of all sizes have an appropriate brush. The toothbrush is also long and slender to ensure that it easily reaches even the farthest molars in your dogs mouth without causing you or your dog undue discomfort.</p>
  <p>The PHS Ora-Clens Dual Ended Toothbrush is flexible and lightweight to ensure that brushing your dog's teeth is a simple task. The toothbrush also features a design pattern of raised paws for a simple but fun decorative flair. Use the brushes with PHS Ora-Clens Dental Gel Toothpaste for comprehensive dental care. Cleaning your dog's teeth has never been easier than with the Ora-Clens Toothbrush</p>
